package Day1;

public class NewLines {
    public static void  main (String[]args){
        System.out.println("Hi this is Prathamesh");
        System.out.println("Hi this ");
        System.out.println("is Prathamesh");
        System.out.print("Hi this");
        System.out.print("is Prathamesh");
        System.out.println("\n"); //  \n Used for new lines
        System.out.println("Hi this is\tPrathamesh"); //  \t used for tab

        //Printing 1 2 3 4 5 in horizontal and vertically
        System.out.println("\nPrinting 1 2 3 4 5 in horizontal and vertically:\n");
        //Method 1
        System.out.println("1 2 3 4 5");
        System.out.println("\n");
        System.out.println("1\n2\n3\n4\n5\n");

        //Method 2
        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4 ");
        System.out.print("5 ");

        System.out.println("\n1");
        System.out.println("2");
        System.out.println("3");
        System.out.println("4");
        System.out.println("5");


    }
}
