package Day2;

public class RelationalOperators {
    public static void main(String[]args){
        System.out.println("Relational operators:\n");

        int Num_1= 35;
        int Num_2= 15;
        System.out.println("Value of Num_1 = 35\nValue of Num_2 = 15\n");
        System.out.println("Num_1 > Num2 = "+(Num_1 > Num_2));
        System.out.println("Num_1 < Num2 = "+(Num_1< Num_2));
        System.out.println("Num_1 >= Num2 = "+(Num_1 >= Num_2));
        System.out.println("Num_1 <= Num2 = "+(Num_1 <= Num_2));
        System.out.println("Num_1 == Num2 = "+(Num_1 == Num_2));
        System.out.println("Num_1 != Num2 = "+(Num_1 != Num_2));
    }
}
