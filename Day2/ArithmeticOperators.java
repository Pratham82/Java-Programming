package Day2;

public class ArithmeticOperators {
    public static void main(String[]args){
        System.out.println("Arithmetic operators:\n");

        int Num_1= 35;
        int Num_2= 15;

        System.out.println("Value of Num_1 =3 5\nValue of Num_2 = 15\n");
        System.out.println("Addition of Num_1 and Num_2 = "+(Num_1+Num_2));
        System.out.println("Subtraction of Num_1 and Num_2 = "+(Num_1-Num_2));
        System.out.println("Multiplication of Num_1 and Num_2 = "+(Num_1*Num_2));
        System.out.println("Division of Num_1 and Num_2 = "+(Num_1/Num_2));
        System.out.println("Modulus of Num_1 and Num_2 = "+(Num_1%Num_2));
    }
}
